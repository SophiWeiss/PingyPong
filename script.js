document.getElementById("preferences-button").addEventListener("click", function() {
  let preferencesMenu = document.getElementById("preferences-menu");
  if (preferencesMenu.style.display === "none") {
    preferencesMenu.style.display = "block";
  } else {
    preferencesMenu.style.display = "none";
  }
});
